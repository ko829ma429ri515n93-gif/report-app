━━━━━━━━━━━━━━━━━━━━━━
- 各本論段落に**最低2つ**の数値データを盛り込む
- 数値は「2025年〜2026年の推定値・予測値」を積極的に使用する
- 形式：「2025年時点で約〇〇%（業界推計）」「2026年までに〇〇億円規模に達する見通し（〇〇研究所予測）」のように出典の種類も明示する
