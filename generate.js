- 全体を通じて「効率・競争優位・合理的判断」を重んじるトーンを維持する
- お行儀のよい優等生的まとめを禁じる。鋭い切り口・独自視点で各段落を締める
- 結論の最後の一文は、読者に行動・思考の変容を迫る断言で終わる

